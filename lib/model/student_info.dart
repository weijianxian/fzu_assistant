class StudentInfo {
  final String name;
  final String sex;
  final String birthday;
  final String phone;
  final String email;
  final String college;
  final String grade;
  final String major;
  final String counselor;
  final String examineeCategory;
  final String nationality;
  final String country;
  final String politicalStatus;
  final String source;
  final String statusChanges;

  const StudentInfo({
    required this.name,
    required this.sex,
    required this.birthday,
    required this.phone,
    required this.email,
    required this.college,
    required this.grade,
    required this.major,
    required this.counselor,
    required this.examineeCategory,
    required this.nationality,
    required this.country,
    required this.politicalStatus,
    required this.source,
    required this.statusChanges,
  });
}
